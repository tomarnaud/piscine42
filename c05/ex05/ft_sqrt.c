int	ft_sqrt(int nb)
{
	int			i;
	long int	check;

	if (nb <= 0)
		return (0);
	i = 0;
	check = 0;
	while (check != nb && i <= (nb / 2) && i < 46340)
	{
		i++;
		check = i * i;
	}
	if (check == nb)
		return (i);
	else
		return (0);
}	
